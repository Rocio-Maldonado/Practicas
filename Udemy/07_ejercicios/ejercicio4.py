"""
Ejercicio 4. Pedir dos numeros al usuario y hacer
todas las operaciones basicas de una calculadora
y mostrarlo por pantalla.
"""

n1 = int(input("Inserte el numero 1: "))
n2 = int(input("Inserte el numero 2: "))

print("***********Calculadora***********")
print(f"{n1} + {n2} = {n1+n2}")
print(f"{n1} - {n2} = {n1-n2}")
print(f"{n1} x {n2} = {n1*n2}")
print(f"{n1} / {n2} = {n1/n2}")
print("***************FIN***************")



