#Operadores Aritmeticos
from __future__ import division


numero1 = 80
numero2 = 4 #Operador de asignacion =

resta = numero1 - numero2
multiplicacion = numero1 * numero2
division = numero1 / numero2
residuo = numero1 % numero2

print("**********CALCULADORA************")
print(f"\t La resta es: {resta}")
print(f"\t La suma es: {numero1 + numero2}")
print(f"\t La multiplicacion es: {multiplicacion}")
print("\t La division es:", division)
print(f"\t El residuo es: {residuo}")